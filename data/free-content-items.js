export function getAllFreeContentItems() {
    return [...freeContentItems];
}

export function getFreeContentItemBySlug(slug) {
    return freeContentItems.find((item) => item.slug === slug) ?? null;
}

export function getRelatedFreeContentItems(slug, limit = 3) {
    const currentItem = getFreeContentItemBySlug(slug);

    if (!currentItem) {
        return freeContentItems.slice(0, limit);
    }

    const orderedRelated = (currentItem.relatedSlugs || [])
        .map((relatedSlug) => getFreeContentItemBySlug(relatedSlug))
        .filter(Boolean);

    const orderedSlugSet = new Set(orderedRelated.map((item) => item.slug));
    const fallback = freeContentItems.filter((item) => item.slug !== slug && !orderedSlugSet.has(item.slug));

    return [...orderedRelated, ...fallback].slice(0, limit);
}

export function getFreeContentImage(item) {
    if (!item?.slug) {
        return defaultImage;
    }

    const imageName = freeContentImageNameOverrides[item.slug] || item.slug;
    const imagePath = `/images/free-content/${imageName}.png`;
    return item.image || imagePath;
}

